import UIKit

class SweepViewController: BaseViewController {

    override func viewDidLoad() {
        super.viewDidLoad()

        // Do any additional setup after loading the view.
    }

    @IBAction func sweepHandleAction(sender: UIButton) {
        let sweep = TempViewController()
        sweep.isHome = false
        hidesBottomBarWhenPushed = true
        navigationController?.pushViewController(sweep, animated: true)
    }


}
